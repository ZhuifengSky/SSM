package com.main.spider.bean;

import com.main.common.bean.BaseSpiderEntity;

public class NovelBean extends BaseSpiderEntity{

	private String title;   //标题
	private String type;    //所属类别
	private String status;  //状态
	private String author;  //作者
	private String coverImage; //封面
	private String description; //简单描述
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getCoverImage() {
		return coverImage;
	}
	public void setCoverImage(String coverImage) {
		this.coverImage = coverImage;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
